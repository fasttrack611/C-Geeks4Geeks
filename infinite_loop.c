#include <stdio.h>

int main()
{
    // Infinite loop using for
    for (;;) {
        printf("Infinite-loop using for\n");
    }

    // Infinite loop using while
    while (1) {
        printf("Infinite-loop using while\n");
    }

    // Infinite loop using do-while
    do {
        printf("Infinite-loop using do-while\n");
    } while (1);

    return 0;
}
